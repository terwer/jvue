--创建临时表空间
CREATE TEMPORARY TABLESPACE BGDATA_TEMP TEMPFILE '/orcl/app/oracle/oradata/orcl/temp02.dbf' SIZE 512M REUSE AUTOEXTEND ON NEXT 50M MAXSIZE UNLIMITED;

--创建数据表空间
CREATE TABLESPACE BGDATA DATAFILE '/orcl/app/oracle/oradata/orcl/bg_data.dbf' SIZE 50M REUSE AUTOEXTEND ON NEXT 50M MAXSIZE UNLIMITED;

--删除表空间
--DROP TABLESPACE BGDATA INCLUDING CONTENTS AND DATAFILES;

-- Create the user
-- USER SQL
CREATE USER "BG" IDENTIFIED BY "cms2019"
DEFAULT TABLESPACE "BGDATA"
TEMPORARY TABLESPACE "BGDATA_TEMP";

-- ROLES
GRANT "DBA" TO "BG" ;
GRANT "CONNECT" TO "BG" ;
GRANT "RESOURCE" TO "BG" ;

-- SYSTEM PRIVILEGES
GRANT ALTER ANY INDEX TO "BG" ;
GRANT CREATE ANY INDEX TO "BG" ;
GRANT CREATE VIEW TO "BG" ;
GRANT ALTER ANY TABLE TO "BG" ;
GRANT DELETE ANY TABLE TO "BG" ;
GRANT DROP ANY TABLE TO "BG" ;
GRANT DROP ANY INDEX TO "BG" ;
GRANT DROP ANY VIEW TO "BG" ;
GRANT CREATE ANY VIEW TO "BG" ;
GRANT CREATE ANY TABLE TO "BG" ;
